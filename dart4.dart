//Dynamic Variable Type

void main() {
  dynamic dynamicVarible = 42; // Intially an integer
  print(dynamicVarible); // Output:42

  dynamicVarible ="Hello Dart!"; //Now a string
  print(dynamicVarible); // Output:Hello, Dart!

  dynamicVarible = [1, 2, 3] ; //Now alist
  print(dynamicVarible); // Output: [1, 2, 3] 
}